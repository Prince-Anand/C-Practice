#include <stdio.h>

int main()
{
    int a, b; // declaring data type for variable a and b
    a = 10;   // assigning values to variable a and b
    b = 20;
    printf("%d", a + b); // %d is a format specifier used to print output of int data types in C
    return 0;
}

